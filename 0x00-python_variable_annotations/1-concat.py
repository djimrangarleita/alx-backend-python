#!/usr/bin/env python3
"""Module for string manipulation
"""

def concat(str1: str, str2: str) -> str:
    """Concatenate two strings

    Args:
        str1 (str): First string parameter, base
        str2 (str): Second string parameter, to be appended to the 1st

    Returns:
        str: The str2 append to the str1
    """
    return str1 + str2